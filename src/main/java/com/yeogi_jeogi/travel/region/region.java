package com.yeogi_jeogi.travel.region;

public class region {
	private int tTravelNum;
	private String tRegionName;
	private String tName;
	private String tInfo;
	private int tCount;
	private String tImgAdd;
	private int tRcm;
	
	public int gettRcm() {
		return tRcm;
	}
	public void settRcm(int tRcm) {
		this.tRcm = tRcm;
	}
	public String gettInfo() {
		return tInfo;
	}
	public void settInfo(String tInfo) {
		this.tInfo = tInfo;
	}
	public int gettCount() {
		return tCount;
	}
	public void settCount(int tCount) {
		this.tCount = tCount;
	}
	public int gettTravelNum() {
		return tTravelNum;
	}
	public void settTravelNum(int tTravelNum) {
		this.tTravelNum = tTravelNum;
	}
	public String gettRegionName() {
		return tRegionName;
	}
	public void settRegionName(String tRegionName) {
		this.tRegionName = tRegionName;
	}
	public String gettName() {
		return tName;
	}
	public void settName(String tName) {
		this.tName = tName;
	}
	public String gettImgAdd() {
		return tImgAdd;
	}
	public void settImgAdd(String tImgAdd) {
		this.tImgAdd = tImgAdd;
	}
	
	
}
