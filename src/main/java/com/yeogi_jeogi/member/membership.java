package com.yeogi_jeogi.member;

public class membership {
	private int mUserNum;
	private int mGradeNum;
	private String mUserId;
	private String mName;
	private String mEmail;
	private String mPassword;
	private String mPhone;
	private String mMbti;
	
	public int getmUserNum() {
		return mUserNum;
	}
	public void setmUserNum(int mUserNum) {
		this.mUserNum = mUserNum;
	}
	public int getmGradeNum() {
		return mGradeNum;
	}
	public void setmGradeNum(int mGradeNum) {
		this.mGradeNum = mGradeNum;
	}
	public String getmUserId() {
		return mUserId;
	}
	public void setmUserId(String mUserId) {
		this.mUserId = mUserId;
	}
	public String getmName() {
		return mName;
	}
	public void setmName(String mName) {
		this.mName = mName;
	}
	public String getmEmail() {
		return mEmail;
	}
	public void setmEmail(String mEmail) {
		this.mEmail = mEmail;
	}
	public String getmPassword() {
		return mPassword;
	}
	public void setmPassword(String mPassword) {
		this.mPassword = mPassword;
	}
	public String getmPhone() {
		return mPhone;
	}
	public void setmPhone(String mPhone) {
		this.mPhone = mPhone;
	}
	public String getmMbti() {
		return mMbti;
	}
	public void setmMbti(String mMbti) {
		this.mMbti = mMbti;
	}
	
}
