package com.yeogi_jeogi.travel.season;

public class season {

		private int sTravelNum;
		private String sRegionName;
		private String sName;
		private String sInfo;
		private int sCount;
		private String sImgAdd;
		private int sRcm;
		
		public int getsTravelNum() {
			return sTravelNum;
		}
		public void setsTravelNum(int sTravelNum) {
			this.sTravelNum = sTravelNum;
		}
		public String getsRegionName() {
			return sRegionName;
		}
		public void setsRegionName(String sRegionName) {
			this.sRegionName = sRegionName;
		}
		public String getsName() {
			return sName;
		}
		public void setsName(String sName) {
			this.sName = sName;
		}
		public String getsInfo() {
			return sInfo;
		}
		public void setsInfo(String sInfo) {
			this.sInfo = sInfo;
		}
		public int getsCount() {
			return sCount;
		}
		public void setsCount(int sCount) {
			this.sCount = sCount;
		}
		public String getsImgAdd() {
			return sImgAdd;
		}
		public void setsImgAdd(String sImgAdd) {
			this.sImgAdd = sImgAdd;
		}
		public int getsRcm() {
			return sRcm;
		}
		public void setsRcm(int sRcm) {
			this.sRcm = sRcm;
		}
		
	}

